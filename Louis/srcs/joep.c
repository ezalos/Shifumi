#include "../includes/head.h"

char joep(char answer_luigi)
{
		int r_v;

		r_v = ft_random(0, 0, answer_luigi * 10000, 1000);
		r_v %= 3;
		if (!r_v)
			return ('F');
		else if (r_v == 1)
			return ('P');
		else
			return ('S');
}
